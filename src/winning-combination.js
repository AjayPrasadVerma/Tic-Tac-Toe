export function findWinner(board) {
  const winningPositions = [
    // Rows
    [
      [0, 0],
      [0, 1],
      [0, 2],
    ],
    [
      [1, 0],
      [1, 1],
      [1, 2],
    ],
    [
      [2, 0],
      [2, 1],
      [2, 2],
    ],
    // Columns
    [
      [0, 0],
      [1, 0],
      [2, 0],
    ],
    [
      [0, 1],
      [1, 1],
      [2, 1],
    ],
    [
      [0, 2],
      [1, 2],
      [2, 2],
    ],
    // Diagonals
    [
      [0, 0],
      [1, 1],
      [2, 2],
    ],
    [
      [0, 2],
      [1, 1],
      [2, 0],
    ],
  ];

  for (const positions of winningPositions) {
    const [i, j] = positions[0];
    const symbol = board[i][j];
    if (
      symbol !== null &&
      board[positions[1][0]][positions[1][1]] === symbol &&
      board[positions[2][0]][positions[2][1]] === symbol
    ) {
      return symbol; // Winner found
    }
  }

  return null; // No winner found
}
